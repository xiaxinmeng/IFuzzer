    ClassPropertiesAndMethods.assertTrue(l.append == l.append)
    ClassPropertiesAndMethods.assertFalse(l.append != l.append)
    ClassPropertiesAndMethods.assertFalse(l.append == [].append)
    ClassPropertiesAndMethods.assertTrue(l.append != [].append)
    ClassPropertiesAndMethods.assertFalse(l.append == l.pop)
    ClassPropertiesAndMethods.assertTrue(l.append != l.pop)
    ClassPropertiesAndMethods.assertNotOrderable(l.append, l.append)
    ClassPropertiesAndMethods.assertEqual(l.append.__name__, 'append')
    ClassPropertiesAndMethods.assertIs(l.append.__ClassPropertiesAndMethods__, l)
